function accum(s) {
	// your code
    let x = s.toUpperCase()
    let allStr=''
    for(let i = 0 ;i<s.length ; i++){
        let str=''
        for(let k = 0 ;k<=i ; k++){
            if(k==0){str+=x[i]}
            else{str+=x[i].toLowerCase()}
        }if(i<s.length-1){
            str+='-'
        }
        
        allStr+=str
    }
return allStr
}
